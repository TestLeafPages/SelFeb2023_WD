package week1.day2;

public class LearningForLoop {

	public static void main(String[] args) {
		
		//for-ctrl+space
		
		//i-- decrement
		for (int i = 100; i >0; i--) {
			System.out.println(i);
		}

		//i++ increment
		for (int i = 0; i<10; i++) {
			System.out.println(i);
		}
		
	}

}
