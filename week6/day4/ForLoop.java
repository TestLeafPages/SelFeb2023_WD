package baseclass;

public class ForLoop {

	public static void main(String[] args) {
//number  20 to 30
		int week=2;
		int days=7;
		
     for(int i=1;i<=week;i++)
     {
		System.out.println("Week " +i);
       for(int j=1;j<=days;j++) {
    	   System.out.println("Day " +j);
       }//j++
     
     //20  week 1 
     //21    day1
    // 22    day2
        

	}//i++
	}
}
